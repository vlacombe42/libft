#include "libft.h"

int		ft_atoi(const char *nptr)
{
	int		i;
	int		neg;
	long	nb;

	i = 0;
	neg = 1;
	nb = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == ' ')
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
		if (nptr[i++] == '-')
			neg = -1;
	while ((nptr[i]) && (nptr[i] >= '0' && nptr[i] <= '9'))
	{
		nb = nb * 10 + nptr[i++] - '0';
		if (nb < 0)
			return ((neg < 0) ? 0 : -1);
	}
	return (neg * nb);
}

